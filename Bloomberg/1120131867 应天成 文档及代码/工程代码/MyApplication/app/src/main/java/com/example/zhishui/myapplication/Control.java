package com.example.zhishui.myapplication;

public class Control {
	private short controlRoll;
	private short controlPitch;
	private short controlYaw;
	private short controlThrottle;
	private short controlMode;
	private short controlGear;
	
	public short getControlRoll() {
		return controlRoll;
	}
	public void setControlRoll(short controlRoll) {
		this.controlRoll = controlRoll;
	}
	
	public short getControlPitch() {
		return controlPitch;
	}
	public void setControlPitch(short controlPitch) {
		this.controlPitch = controlPitch;
	}
	
	public short getControlYaw() {
		return controlYaw;
	}
	public void setControlYaw(short controlYaw) {
		this.controlYaw = controlYaw;
	}
	
	public short getControlThrottle() {
		return controlThrottle;
	}
	public void setControlThrottle(short controlThrottle) {
		this.controlThrottle = controlThrottle;
	}
	
	public short getControlMode() {
		return controlMode;
	}
	public void setControlMode(short controlMode) {
		this.controlMode = controlMode;
	}
	
	public short getControlGear() {
		return controlGear;
	}
	public void setControlGear(short controlGear) {
		this.controlGear = controlGear;
	}
	
}

package com.example.zhishui.myapplication;

public class GPS {
	
	private double GPSLongti;
	private double GPSLati;
	private float GPSAlti;
	private float GPSHeight;
	private int GPSHealth_flag;
	
	public double getGPSLongti() {
		return GPSLongti;
	}
	public void setGPSLongti(double gPSLongti) {
		GPSLongti = gPSLongti;
	}
	
	public float getGPSAlti() {
		return GPSAlti;
	}
	public void setGPSAlti(float gPSAlti) {
		GPSAlti = gPSAlti;
	}
	
	public double getGPSLati() {
		return GPSLati;
	}
	public void setGPSLati(double gPSLati) {
		GPSLati = gPSLati;
	}
	
	public float getGPSHeight() {
		return GPSHeight;
	}
	public void setGPSHeight(float gPSHeight) {
		GPSHeight = gPSHeight;
	}
	
	public int getGPSHealth_flag() {
		return GPSHealth_flag;
	}
	public void setGPSHealth_flag(int gPSHealth_flag) {
		GPSHealth_flag = gPSHealth_flag;
	}
}
